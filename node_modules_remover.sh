#!/bin/bash

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
NC='\033[0m' # No Color

# Checks if the input directory exists
if [ ! -d "$1" ]; then
echo -e "${RED}Error: $1 is not a directory or does not exist${NC}"
exit 1
fi

if [ -z "$2" ]; then
depth=3
else
if ! [[ "$2" =~ ^[0-9]+$ ]]; then
echo -e "${RED}Error: Invalid value for maximum depth: $2${NC}"
exit 1
fi
depth=$2
fi

# Recursively finds all top-level node_modules folders in the input directory
echo -e "${YELLOW}Searching for node_modules folders in $1... (max depth: $depth)${NC}"
node_modules_folders=$(find "$1" -mindepth 2 -maxdepth "$depth" -name "node_modules" -type d)

if [ $? -ne 0 ]; then
  echo -e "${RED}Error: Failed to search for node_modules folders${NC}"
  exit 1
fi

node_modules_folders=$(echo "$node_modules_folders")
folder_count=$(echo "$node_modules_folders" | wc -l)
if [ -z "$node_modules_folders" ] || [ "$folder_count" -eq 0 ]; then
  echo -e "${RED}No node_modules folders found in $1${NC}"
  exit 0
fi
# Calculates the total size of all node_modules folders
total_size=$(du -shc $(echo "$node_modules_folders") | tail -1 | awk '{print $1}')

# Prints the summary
echo -e "${GREEN}Found $folder_count node_modules of total size $total_size in the following folders:${NC}"
index=0
while read folder; do
project_folder=$(dirname "$folder")
project_name=$(basename "$project_folder")
size=$(du -sh "$folder" | awk '{print $1}')
echo "[$index] $project_name - $size"
index=$((index+1))
done <<< "$node_modules_folders"

# Prompts the user to select node_modules folders to delete
echo -e "\nEnter the index number(s) of the node_modules folder(s) to delete (separated by a space).\nTo delete all, press enter. To quit, type 'q':"
read -a folders_to_delete
if [ "${folders_to_delete[0]}" == "q" ]; then
echo "Aborted"
exit 0
elif [ ${#folders_to_delete[@]} -eq 0 ]; then
folders_to_delete=($(seq 0 $((folder_count-1))))
fi

# Deletes the node_modules folders
total_size_deleted=0
for i in "${folders_to_delete[@]}"; do
if [ $i -lt 0 ] || [ $i -ge $folder_count ]; then
echo "Error: Invalid index: $i"
continue
fi
folder=$(echo "$node_modules_folders" | sed -n "$((i+1))p")
project_folder=$(dirname "$folder")
project_name=$(basename "$project_folder")
size=$(du -sh "$folder" | awk '{print $1}')
echo "Removing node_modules folder for $project_name ($size)..."
rm -rf "$folder"
size_in_bytes=$(numfmt --from=iec $size)
total_size_deleted=$(echo $total_size_deleted + $size_in_bytes | bc)
done

echo -e "${GREEN}Done, Total space saved: $(numfmt --to=iec-i --suffix=B $total_size_deleted)${NC}"
